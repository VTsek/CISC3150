import java.util.Scanner;
 
public class Redirection {
 
 
 
        public static void main(String[] args) {
 
               
 
                Scanner sc = new Scanner(System.in);
 
               
 
                do{
 
                       
 
                        System.out.print(sc.next());
 
                }while(sc.hasNext());
 
               
 
 sc.close();
 
        }
 
}